function m=repmat_zh(n,num)
%==========================================================================
%  m=repmat_zh(n,num)
%  to make m=[n(1,:);n(1,:)...num...;n(1,:);n(2,:)...]
%--------------------------------------------------------------------------
%  Input
%       n: a column vector or a matrix
%     num: decide how many times each row of 'n' be copied
% Output
%      m: has the size of [size(n,1)*num,size(n,2)]
%--------------------------------------------------------------------------
%       Zhang Yong, 2012-02-09 12:44, GFZ, Potsdam
%==========================================================================
[~,r]=size(n);
n=repmat(n',[num,1]);
m=reshape(n,r,[])';
return
%==========================================================================